function font.createTexture()
    local texWidth = 128
    local texHeight = 64
    local charWidth = 8
    local charHeight = 8
    local charsPerRow = 16
    
    local texData = ffi.new("unsigned char[?]", texWidth * texHeight * 4)
    
    for c = 32, 126 do
        local bitmap = fontBitmaps[c]
        if bitmap then
            local idx = c - 32
            local col = idx % charsPerRow
            local row = math.floor(idx / charsPerRow)
            local xBase = col * charWidth
            local yBase = row * charHeight
            
            for x = 0, 4 do
                local byte = bitmap[x + 1]
                for y = 0, 7 do
                    if bit.band(byte, bit.lshift(1, y)) ~= 0 then
                        local tx = xBase + x + 1
                        local ty = yBase + (7 - y)
                        local p = (ty * texWidth + tx) * 4
                        texData[p] = 255
                        texData[p+1] = 255
                        texData[p+2] = 255
                        texData[p+3] = 255
                    end
                end
            end
        end
    end
    
    local id = ffi.new("GLuint[1]")
    gl.glGenTextures(1, id)
    gl.glBindTexture(gl.TEXTURE_2D, id[0])
    
    gl.glPixelStorei(gl.UNPACK_ALIGNMENT, 1)
    
    gl.glTexImage2D(gl.TEXTURE_2D, 0, gl.RGBA, texWidth, texHeight, 0, gl.RGBA, gl.UNSIGNED_BYTE, texData)
    gl.glTexParameteri(gl.TEXTURE_2D, gl.TEXTURE_MIN_FILTER, 0x2600)
    gl.glTexParameteri(gl.TEXTURE_2D, gl.TEXTURE_MAG_FILTER, 0x2600)
    gl.glTexParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_S, 0x812F)
    gl.glTexParameteri(gl.TEXTURE_2D, gl.TEXTURE_WRAP_T, 0x812F)
    
    return {
        id = id[0], 
        texWidth = texWidth, 
        texHeight = texHeight, 
        charWidth = charWidth, 
        charHeight = charHeight,
        charsPerRow = charsPerRow
    }
end

return font
